public struct GestureFrame
{
    public GestureFrame(int timestamp, float x, float y, float z)
    {
        this.timestamp = timestamp;
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int timestamp { get; }
    public float x { get; }
    public float y { get; }
    public float z { get; }

    public override string ToString() => $"({timestamp}, {x}, {y}, {z})";
}
